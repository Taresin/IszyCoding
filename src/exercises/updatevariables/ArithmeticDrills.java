package exercises.updatevariables;

public class ArithmeticDrills {
    public static void main(String[] args) {

        // Multiplication
        int answer = 3 * 4;
        System.out.println(answer);

        // Addition
        answer = 5 + 3;
        System.out.println(answer);

        // Subtraction
        answer = 3 - 4;
        System.out.println(answer);

        // Division
        answer = 10 / 2;
        System.out.println(answer);


        // Mod (%) the remainder
        // (this is what we want to practice)
        answer = 10 % 2;
        System.out.println(answer);
        answer = 5 % 2;
        System.out.println(answer);

//        I want you to keep updating the storage box with a new value and seeing what the answer is.
//        Try to guess the answer before you run it.
//                I would like you to practice setting the variable to a value that is calculated
//        At the same time, practice the mod function (%) so you get used to mod 2 (i.e. 10 % 2 and 37 % 2 and 14 % 2)
//        a little explanation: every time you get x % 2 the number evaluates to 0 if x is even and 1 if x is odd. that's the rule.
//        so just like how you see 3 + 4 and you go oh that's just 7
//        you can see 8 % 2 and you can go oh that's just 0 because 8 is an even number.
//        i know u understand it and it's really simple.
//        but do 10 of the % statements with random numbers (get a mix of odd and even numbers and print it out.
    }
}
