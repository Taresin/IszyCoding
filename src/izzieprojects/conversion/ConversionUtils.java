package izzieprojects.conversion;

public class ConversionUtils {

    public static double poundsToGrams(double pounds) {
        return pounds * 1000 / 2.2 ;
    }
    public static double teaspoonToMls(double teaspoon) {
        return teaspoon * 5;
    }
    public static double stickToGrams(double stick) {
        return stick * 135;
    }
    public static double tablespoonToMls(double tablespoon) {
        return tablespoon * 10;
    }
    public static double ouncesToGrams(double ounces) {
        return ounces * 28;
    }
}
